// Типы данных для калькулятора материалов дома

// Технологии строительства
export type BuildingTechnology = 
  | 'frame'        // Каркасный дом
  | 'sip'          // SIP панели
  | 'gasobeton'    // Газобетон
  | 'concrete_slab' // Ж/Б плиты
  | 'log'          // Бревенчатый дом (сруб)
  | 'brick';       // Кирпичный дом

export interface HouseParams {
  // Основные параметры дома
  length: number;          // длина дома (м)
  width: number;           // ширина дома (м)
  floorHeight: number;     // высота этажа (м)
  floors: number;          // количество этажей
  hasAttic: boolean;       // мансарда
  
  // Технология строительства
  buildingTechnology: BuildingTechnology;
  
  // Параметры крыши
  roofType: 'gable' | 'shed' | 'hip';  // тип крыши
  roofAngle: number;       // угол наклона (градусы)
  
  // Стены и перегородки
  externalWalls: number;   // количество внешних стен
  internalWalls: number;   // длина внутренних несущих стен (м)
  externalWallThickness: number;  // толщина внешних стен (мм)
  internalWallThickness: number;  // толщина внутренних стен (мм)
  studSpacing: number;     // шаг стоек (мм)
  
  // Окна и двери
  smallWindows: number;    // малые окна
  mediumWindows: number;   // средние окна
  largeWindows: number;    // большие окна
  externalDoors: number;   // внешние двери
  internalDoors: number;   // внутренние двери
  
  // Фундамент
  foundationType: 'pile' | 'strip' | 'slab';
  foundationDepth: number; // глубина/высота фундамента (м)
  
  // Дополнительные опции
  insulationType: 'mineral' | 'eco' | 'foam';
  insulationThickness: number;  // толщина утеплителя (мм)
  exteriorFinish: 'siding' | 'blockhouse' | 'plaster';
  roofingMaterial: 'metal' | 'shingle' | 'profile';
  
  // Специфичные параметры для разных технологий
  logDiameter?: number;       // диаметр бревна (мм) - для log
  brickType?: 'solid' | 'hollow' | 'facing';  // тип кирпича
  sipThickness?: number;      // толщина SIP панели (мм)
  gasobetonDensity?: number;  // плотность газобетона (D400, D500, D600)
}

export interface MaterialItem {
  category: string;
  name: string;
  quantity: number;
  unit: string;
  notes?: string;
  unitPrice?: number;  // цена за единицу
  totalPrice?: number; // общая сумма
}

export interface CalculationResult {
  materials: MaterialItem[];
  summary: {
    totalLumberVolume: number;
    totalInsulationVolume: number;
    totalRoofArea: number;
    totalWallArea: number;
    totalFloorArea: number;
    totalCost: number;  // общая стоимость
  };
}

